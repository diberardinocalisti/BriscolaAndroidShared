package multiplayer;

public class User {

    private int vinte;
    private int perse;

    public User(int vinte, int perse) {
        this.vinte = vinte;
        this.perse = perse;
    }

    public int getVinte() {
        return vinte;
    }

    public void setVinte(int vinte) {
        this.vinte = vinte;
    }

    public int getPerse() {
        return perse;
    }

    public void setPerse(int perse) {
        this.perse = perse;
    }
}


